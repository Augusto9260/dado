function carrega() {
	let numeros = Math.floor (Math.random() * 6) + 1;
	let img = document.getElementById('img');

	if (numeros == 1) {
		img.setAttribute('src', 'um.png');
	}
	else if (numeros == 2) {
		img.setAttribute('src', 'dois.png');
	}
	else if (numeros == 3) {
		img.setAttribute('src', 'tres.png');
	}
	else if (numeros == 4) {
		img.setAttribute('src', 'quatro.png');
	}
	else if (numeros == 5) {
		img.setAttribute('src', 'cinco.png');
	}
	else if (numeros == 6) {
		img.setAttribute('src', 'seis.png');
	}
}
